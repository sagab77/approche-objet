package entites;

public class Personne {

    public String nom, prenom, adresse;


    public Personne(String nom, String prenom) {
        this.nom = nom;
        this.adresse = adresse;
    }

    public Personne(String nom, String prenom, String adresse) {
        this.nom = nom;
        this.prenom = prenom;
        this.adresse = adresse;
    }


}
